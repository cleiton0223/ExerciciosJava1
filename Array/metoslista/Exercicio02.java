package aula10_06_25;

//Crie um método que exiba todos os números pares de 0 até 100 usando um
//laço for.
public class Exercicio02 {

    public static void numeroPares() {

        for (int i = 0; i < 101; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }
    }
}
            

        
       
       
    

    

